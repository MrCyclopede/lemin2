#include "libft.h"

void	ft_putnbr(int n)
{
	long int x;

	x = n;
	if (x < 0)
	{
		ft_putchar('-');
		x = -x;
	}
	if (x >= 10)
		ft_putnbr(x / 10);
	ft_putchar((x % 10) + '0');
}
