#include "libft.h"

void	*ft_memmove(void *dst, const void *src, size_t len)
{
	if (dst > src)
		while (len--)
			((unsigned char *)dst)[len] = ((const unsigned char *)src)[len];
	else if (dst < src)
		ft_memcpy(dst, src, len);
	return (dst);
}
